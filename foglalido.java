package sample;

public class foglalido {

    String nev;
    String tol;
    String dig;

    public foglalido(String nev, String tol, String dig) {
        this.nev = nev;
        this.tol = tol;
        this.dig = dig;
    }

    public String getNev() {
        return nev;
    }
    public String getTol() {
        return tol;
    }
    public String getDig(){
        return dig;
    }




}

package sample;

public class users {

    String name;
    String age;
    String gender;
    String freedom;
    String id;

    public users(String name, String age, String gender,String freedom, String id) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.freedom = freedom;
        this.id = id;
    }

    public String getName() {
        return name;
    }
    public String getAge() {
        return age;
    }
    public String getGender(){
        return gender;
    }
    public String getFreedom() {
        return freedom;
    }
    public String getId() { return id; }




}
